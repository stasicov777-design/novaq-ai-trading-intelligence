def normalize_lang(lang: str | None) -> str:
    if lang in SUPPORTED_LANGS:
        return lang

    return "en"


def get_lang_from_request(request: Request) -> str:
    query_lang = request.query_params.get("lang")
    if query_lang:
        return normalize_lang(query_lang)

    return normalize_lang(request.cookies.get(LANG_COOKIE_NAME))


def lang_switch_url(path: str, lang: str) -> str:
    return f"{path}?lang={normalize_lang(lang)}"


def t(lang: str, key: str) -> str:
    normalized = normalize_lang(lang)
    return TRANSLATIONS.get(normalized, {}).get(key) or TRANSLATIONS["en"].get(key) or key
